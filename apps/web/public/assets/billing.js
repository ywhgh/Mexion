var BALANCE = 0;
var BALANCE_LOADED = false;
var LAST_TOPUP_AMT = 0;
var BILLING_TOPUP_SYNC_TIMER = null;
var BILLING_POLL_TIMER = null;
var BILLING_POLL_INTERVAL = 15000;
var BILLING_POLL_INFLIGHT = false;
var BILLING_POLL_DEBOUNCE_TIMER = null;

function setBalanceFromUser(user) {
  BALANCE = typeof window.MexionQuota !== 'undefined'
    ? window.MexionQuota.getUserBalance(user)
    : ((Number(user && user.quota) || 0) / 500000);
}

function applyBillingUser(user, animate) {
  if (!user) return;
  setBalanceFromUser(user);
  BALANCE_LOADED = true;
  updateBalanceDisplay(!!animate);
  if (typeof window.__billingUpdateTopupUI === 'function') window.__billingUpdateTopupUI();
}

function getBillingLang() {
  return (typeof MexionI18n !== 'undefined' && MexionI18n.lang) ? MexionI18n.lang : 'zh';
}

function billingEsc(value) {
  return String(value == null ? '' : value).replace(/[&<>"]/g, function(ch) {
    return ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' })[ch];
  });
}

function formatQuotaUnits(value) {
  var num = Number(value || 0);
  if (!isFinite(num)) num = 0;
  return Math.round(num).toLocaleString();
}

function formatSubscriptionDate(value) {
  var d = value ? new Date(value) : null;
  if (!d || isNaN(d.getTime())) return '—';
  return d.getFullYear() + '-' + String(d.getMonth() + 1).padStart(2, '0') + '-' + String(d.getDate()).padStart(2, '0');
}

function ensureSubscriptionProgressCard() {
  var card = document.getElementById('subProgressCard');
  if (card) return card;
  var hero = document.querySelector('.bill-hero');
  var actionGrid = document.querySelector('.action-grid');
  card = document.createElement('section');
  card.className = 'card sub-progress-card fade-in fade-in--3';
  card.id = 'subProgressCard';
  card.innerHTML =
    '<div class="card__head">' +
      '<span class="card__title" id="subProgressTitle">§ 订阅进度</span>' +
      '<span class="card__title-meta" id="subProgressMeta">—</span>' +
    '</div>' +
    '<div class="sub-summary-grid" id="subSummaryGrid"></div>' +
    '<div class="sub-progress-list" id="subProgressList"></div>';
  if (actionGrid && actionGrid.parentNode) actionGrid.parentNode.insertBefore(card, actionGrid);
  else if (hero && hero.parentNode) hero.parentNode.appendChild(card);
  return card;
}

function renderSubscriptionProgress(progressData, summaryData) {
  var lang = getBillingLang();
  var card = ensureSubscriptionProgressCard();
  var title = document.getElementById('subProgressTitle');
  var meta = document.getElementById('subProgressMeta');
  var summaryGrid = document.getElementById('subSummaryGrid');
  var list = document.getElementById('subProgressList');
  var progress = Array.isArray(progressData) ? progressData : [];
  var summary = summaryData || {};

  if (title) title.textContent = lang === 'zh' ? '§ 订阅进度' : '§ Subscription progress';
  if (meta) meta.textContent = progress.length + (lang === 'zh' ? ' 个活跃套餐' : ' active plan' + (progress.length === 1 ? '' : 's'));

  if (summaryGrid) {
    var pct = Math.round(Number(summary.overallPercent || 0));
    summaryGrid.innerHTML =
      '<div class="sub-summary-cell"><span>' + (lang === 'zh' ? '总额度' : 'Total quota') + '</span><b>' + formatQuotaUnits(summary.totalQuota) + '</b></div>' +
      '<div class="sub-summary-cell"><span>' + (lang === 'zh' ? '已用' : 'Used') + '</span><b>' + formatQuotaUnits(summary.totalUsed) + '</b></div>' +
      '<div class="sub-summary-cell"><span>' + (lang === 'zh' ? '剩余' : 'Remaining') + '</span><b>' + formatQuotaUnits(summary.totalRemaining) + '</b></div>' +
      '<div class="sub-summary-cell"><span>' + (lang === 'zh' ? '总体进度' : 'Overall') + '</span><b>' + pct + '%</b></div>' +
      '<div class="sub-summary-cell sub-summary-cell--wide"><span>' + (lang === 'zh' ? '最早到期' : 'Earliest expiry') + '</span><b>' + formatSubscriptionDate(summary.earliestExpiry) + '</b></div>';
  }

  if (!list) return;
  if (!progress.length) {
    card.hidden = false;
    list.innerHTML = '<div class="sub-progress-empty">' + (lang === 'zh' ? '暂无活跃订阅，启用套餐后这里会显示额度进度。' : 'No active subscriptions. Progress appears here after a plan is activated.') + '</div>';
    return;
  }
  list.innerHTML = progress.map(function(item) {
    var used = Math.max(0, Math.min(100, Number(item.usedPercent || 0)));
    var pctLabel = Math.round(used) + '%';
    var name = billingEsc(item.planName || ('Plan #' + item.planId));
    var days = Number(item.daysLeft || 0);
    var metaText = lang === 'zh'
      ? '剩余 ' + formatQuotaUnits(item.remainingQuota) + ' quota · 还有 ' + days + ' 天到期'
      : formatQuotaUnits(item.remainingQuota) + ' quota left · expires in ' + days + ' day' + (days === 1 ? '' : 's');
    return '<div class="sub-progress-item' + (item.isLow ? ' is-low' : '') + '">' +
      '<div class="sub-progress-header">' +
        '<span class="sub-plan-name">' + name + '</span>' +
        '<span class="sub-progress-pct">' + pctLabel + '</span>' +
      '</div>' +
      '<div class="sub-progress-bar" aria-hidden="true"><div class="sub-progress-fill" style="width:' + used.toFixed(2) + '%"></div></div>' +
      '<div class="sub-progress-meta">' + billingEsc(metaText) + '</div>' +
    '</div>';
  }).join('');
}

function loadSubscriptionProgress() {
  if (typeof MexionHttp === 'undefined') return Promise.resolve();
  return Promise.all([
    MexionHttp.get('/user/subscriptions/progress').catch(function(){ return { progress: [] }; }),
    MexionHttp.get('/user/subscriptions/summary').catch(function(){ return { summary: {} }; })
  ]).then(function(results) {
    var progress = results[0] && (results[0].progress || results[0].items || results[0]);
    var summary = results[1] && (results[1].summary || results[1]);
    renderSubscriptionProgress(progress, summary);
  });
}

function refreshBillingUserState(animate, source) {
  if (typeof MexionAuth !== 'undefined' && MexionAuth.refreshUser) {
    return MexionAuth.refreshUser({ source: source || 'billing-refresh', force: true }).then(function(user){
      applyBillingUser(user, animate);
      return user;
    });
  }
  return MexionHttp.get('/user/self').then(function(data){
    var user = data.user || data;
    applyBillingUser(user, animate);
    return user;
  });
}

function getBillingPayStatus() {
  try {
    return new URLSearchParams(window.location.search).get('pay') || '';
  } catch (err) {
    return '';
  }
}

function clearBillingPayStatus() {
  try {
    var url = new URL(window.location.href);
    if (!url.searchParams.has('pay')) return;
    url.searchParams.delete('pay');
    history.replaceState({}, document.title, url.pathname + (url.search ? url.search : '') + url.hash);
  } catch (err) {}
}

function normalizeBillingPayMethod(method) {
  if (method === 'wechat') return 'wxpay';
  return method || '';
}

function submitBillingPaymentForm(url, params, targetName) {
  var form = document.createElement('form');
  form.action = url;
  form.method = 'POST';
  // 提交到指定命名窗口(支付弹窗)，到账后由 startBillingTopupSync 自动关闭，省去用户手动删多余标签页。
  form.target = targetName || '_blank';
  Object.keys(params || {}).forEach(function(key) {
    if (key === 'url') return;
    var input = document.createElement('input');
    input.type = 'hidden';
    input.name = key;
    input.value = params[key];
    form.appendChild(input);
  });
  document.body.appendChild(form);
  form.submit();
  document.body.removeChild(form);
}

function stopBillingTopupSync() {
  if (BILLING_TOPUP_SYNC_TIMER) {
    clearInterval(BILLING_TOPUP_SYNC_TIMER);
    BILLING_TOPUP_SYNC_TIMER = null;
  }
}

function stopBillingPolling() {
  if (BILLING_POLL_TIMER) {
    clearInterval(BILLING_POLL_TIMER);
    BILLING_POLL_TIMER = null;
  }
}

function pollBillingSnapshot(source) {
  if (typeof MexionHttp === 'undefined') return Promise.resolve();
  if (BILLING_POLL_INFLIGHT) return Promise.resolve();
  BILLING_POLL_INFLIGHT = true;
  return Promise.allSettled([
    refreshBillingUserState(false, source || 'billing-poll'),
    reloadBillingTransactions(),
    loadSubscriptionProgress()
  ]).then(function(r) { BILLING_POLL_INFLIGHT = false; return r; },
         function(e) { BILLING_POLL_INFLIGHT = false; throw e; });
}

function startBillingPolling() {
  stopBillingPolling();
  if (document.hidden) return;
  BILLING_POLL_TIMER = setInterval(function(){
    pollBillingSnapshot('billing-poll');
  }, BILLING_POLL_INTERVAL);
}

function startBillingTopupSync(baselineBalance, payWin) {
  stopBillingTopupSync();
  var attempts = 0;

  function tick() {
    attempts++;
    refreshBillingUserState(false, 'billing-topup-sync').catch(function(){})
      .then(function() {
        return reloadBillingTransactions();
      })
      .finally(function() {
        if (typeof baselineBalance === 'number' && BALANCE > baselineBalance) {
          stopBillingTopupSync();
          // 到账：自动关闭支付弹窗，原页面已就地刷新余额——用户无需手动关多余标签页。
          if (payWin) { try { payWin.close(); } catch (e) {} }
          if (typeof MexionToast !== 'undefined' && MexionToast.show) {
            MexionToast.show(getBillingLang() === 'zh' ? '充值已到账' : 'Top-up credited', { tone: 'success' });
          }
        } else if (attempts >= 30) {
          stopBillingTopupSync();
        }
      });
  }

  tick();
  BILLING_TOPUP_SYNC_TIMER = setInterval(tick, 3000);
}

function setBillingMethodDisabled(button, disabled) {
  if (!button) return;
  button.disabled = !!disabled;
  button.classList.toggle('is-disabled', !!disabled);
  button.style.opacity = disabled ? '0.45' : '';
  button.style.cursor = disabled ? 'not-allowed' : '';
}

function setBillingMethodVisible(button, visible) {
  if (!button) return;
  button.hidden = !visible;
  button.style.display = visible ? '' : 'none';
}

function applyBillingPaymentAvailability(info) {
  // 同步后端最小充值额，避免前端硬编码 min=10 把合法的小额自定义充值（后端默认 min=1）静默拦截
  var minTopup = Number(info && info.min_topup);
  if (!isFinite(minTopup) || minTopup <= 0) minTopup = 1;
  window.__billingMinTopup = minTopup;
  var amtInputEl = document.getElementById('topupAmt');
  if (amtInputEl) amtInputEl.min = String(minTopup);

  var payMethods = Array.isArray(info && info.pay_methods) ? info.pay_methods : [];
  var enabledTypes = {};
  payMethods.forEach(function(method) {
    if (method && method.type) enabledTypes[method.type] = true;
  });

  var buttons = Array.prototype.slice.call(document.querySelectorAll('#methodGrid .method-btn'));
  buttons.forEach(function(button) {
    var type = normalizeBillingPayMethod(button.dataset.m);
    var visible = !!enabledTypes[type];
    setBillingMethodVisible(button, visible);
    setBillingMethodDisabled(button, !visible);
  });

  var hasMethod = !!(info && info.enable_online_topup !== false && payMethods.length > 0);
  var submitBtn = document.getElementById('topupSubmit');
  var submitLabel = document.getElementById('submitLabel');
  submitBtn.dataset.paymentDisabled = hasMethod ? 'false' : 'true';
  submitBtn.disabled = !hasMethod;
  submitBtn.style.opacity = hasMethod ? '' : '0.45';
  submitBtn.style.cursor = hasMethod ? '' : 'not-allowed';
  if (!hasMethod && submitLabel) {
    submitLabel.textContent = getBillingLang() === 'zh' ? '暂未开放充值' : 'Top-up not available';
  }

  var active = document.querySelector('#methodGrid .method-btn.is-active');
  if (!active || active.disabled || active.hidden) {
    buttons.forEach(function(button) { button.classList.remove('is-active'); });
    var firstEnabled = buttons.find(function(button) { return !button.disabled && !button.hidden; });
    if (firstEnabled) firstEnabled.classList.add('is-active');
  }
}

