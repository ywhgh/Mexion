/* Helper: format date string to MM-DD */
function parseBillingDate(value){
  if (!value && value !== 0) return null;
  if (value instanceof Date) return isNaN(value.getTime()) ? null : value;
  var raw = value;
  if (typeof raw === 'string') {
    var trimmed = raw.trim();
    if (/^\d+$/.test(trimmed)) raw = Number(trimmed);
    else {
      var strDate = new Date(trimmed);
      return isNaN(strDate.getTime()) ? null : strDate;
    }
  }
  if (typeof raw === 'number') {
    var num = raw;
    if (num > 1e12) return new Date(num);
    if (num > 1e9) return new Date(num * 1000);
  }
  var date = new Date(raw);
  return isNaN(date.getTime()) ? null : date;
}

function fmtDate(s){
  if (!s) return '—';
  var d = parseBillingDate(s);
  if (!d) return '—';
  return String(d.getMonth()+1).padStart(2,'0') + '-' + String(d.getDate()).padStart(2,'0');
}
function fmtTime(s){
  if (!s) return '';
  var d = parseBillingDate(s);
  if (!d) return '';
  return String(d.getHours()).padStart(2,'0') + ':' + String(d.getMinutes()).padStart(2,'0');
}
function fmtMonthKey(s){
  if (!s) return '';
  var d = parseBillingDate(s);
  if (!d) return '';
  return d.getFullYear() + '-' + String(d.getMonth()+1).padStart(2,'0');
}
function fmtCsvDate(s){
  var d = parseBillingDate(s);
  if (!d) return '';
  return d.getFullYear() + '-' + String(d.getMonth()+1).padStart(2,'0') + '-' + String(d.getDate()).padStart(2,'0');
}
