function initBillingTxnDateFilter() {
  var fromInput = document.getElementById('txnDateFrom');
  var toInput = document.getElementById('txnDateTo');
  var fromField = document.getElementById('txnDateFromField');
  var toField = document.getElementById('txnDateToField');
  var quickWrap = document.getElementById('txnQuickRanges');
  setBillingTxnDateRange('', '');
  if (fromField) {
    var openFrom = function(){ openBillingDatePicker('txnDateFrom'); };
    fromField.addEventListener('click', openFrom);
    fromField.addEventListener('keydown', function(e){
      if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); openFrom(); }
    });
  }
  if (toField) {
    var openTo = function(){ openBillingDatePicker('txnDateTo'); };
    toField.addEventListener('click', openTo);
    toField.addEventListener('keydown', function(e){
      if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); openTo(); }
    });
  }
  if (fromInput) fromInput.addEventListener('change', function(){
    var nextFrom = fromInput.value || '';
    var nextTo = toInput ? (toInput.value || '') : '';
    if (nextFrom && nextTo && nextFrom > nextTo) nextTo = nextFrom;
    setBillingTxnDateRange(nextFrom, nextTo);
  });
  if (toInput) toInput.addEventListener('change', function(){
    var nextFrom = fromInput ? (fromInput.value || '') : '';
    var nextTo = toInput.value || '';
    if (nextFrom && nextTo && nextFrom > nextTo) nextFrom = nextTo;
    setBillingTxnDateRange(nextFrom, nextTo);
  });
  if (quickWrap) quickWrap.addEventListener('click', function(e){
    var button = e.target.closest('.txn-quick-range');
    if (!button) return;
    var range = button.getAttribute('data-range');
    var end = new Date();
    var start = new Date();
    if (range === 'today') {
      setBillingTxnDateRange(fmtCsvDate(end), fmtCsvDate(end));
      return;
    }
    if (range === 'yesterday') {
      start.setDate(start.getDate() - 1);
      setBillingTxnDateRange(fmtCsvDate(start), fmtCsvDate(start));
      return;
    }
    if (range === '3d') start.setDate(start.getDate() - 2);
    if (range === '7d') start.setDate(start.getDate() - 6);
    if (range === '30d') start.setDate(start.getDate() - 29);
    setBillingTxnDateRange(fmtCsvDate(start), fmtCsvDate(end));
  });
}

