function syncBillingQuickRangeState() {
  var buttons = document.querySelectorAll('#txnQuickRanges .txn-quick-range');
  buttons.forEach(function(button){
    var range = button.getAttribute('data-range');
    var active = false;
    var today = fmtCsvDate(new Date());
    if (range === 'today') active = CURRENT_DATE_FROM === today && CURRENT_DATE_TO === today;
    if (range === 'yesterday') {
      var y = new Date();
      y.setDate(y.getDate() - 1);
      var yv = fmtCsvDate(y);
      active = CURRENT_DATE_FROM === yv && CURRENT_DATE_TO === yv;
    }
    if (range === '3d') {
      var d3 = new Date();
      d3.setDate(d3.getDate() - 2);
      active = CURRENT_DATE_FROM === fmtCsvDate(d3) && CURRENT_DATE_TO === today;
    }
    if (range === '7d') {
      var d7 = new Date();
      d7.setDate(d7.getDate() - 6);
      active = CURRENT_DATE_FROM === fmtCsvDate(d7) && CURRENT_DATE_TO === today;
    }
    if (range === '30d') {
      var d30 = new Date();
      d30.setDate(d30.getDate() - 29);
      active = CURRENT_DATE_FROM === fmtCsvDate(d30) && CURRENT_DATE_TO === today;
    }
    button.classList.toggle('is-active', active);
  });
}

