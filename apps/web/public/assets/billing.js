document.addEventListener('DOMContentLoaded', function(){
  if (typeof MexionHttp === 'undefined') return;
  var lang = getBillingLang();
  var payStatus = getBillingPayStatus();
  initBillingTxnDateFilter();

  if (typeof MexionAuth !== 'undefined' && MexionAuth.fetchUser) {
    MexionAuth.fetchUser().then(function(user){
      applyBillingUser(user, true);
      if (payStatus === 'success' || payStatus === 'pending') startBillingTopupSync(BALANCE);
    }).catch(function(){});
  } else {
    MexionHttp.get('/user/self').then(function(data){
      var user = data.user || data;
      applyBillingUser(user, true);
      if (payStatus === 'success' || payStatus === 'pending') startBillingTopupSync(BALANCE);
    }).catch(function(){});
  }

  window.addEventListener('mexion:user-updated', function (event) {
    var user = event && event.detail ? event.detail.user : null;
    if (!user) return;
    applyBillingUser(user, false);
  });

  Promise.resolve({ enable_online_topup: false, pay_methods: [] }).then(function(info){
    applyBillingPaymentAvailability(info || {});
    var submitBtn = document.getElementById('topupSubmit');
    if (submitBtn) { submitBtn.disabled = false; submitBtn.dataset.paymentDisabled = 'demo'; submitBtn.style.opacity = ''; submitBtn.style.cursor = ''; }
  });

  Promise.all([
    MexionHttp.get('/subscription/plans').catch(function(){ return { plans: [] }; }),
    MexionHttp.get('/user/subscriptions').catch(function(){ return { subscriptions: [] }; })
  ]).then(function(){ loadSubscriptionProgress(); });
  reloadBillingTransactions();
  startBillingPolling();

  function debouncedPollOnVisible(source) {
    if (BILLING_POLL_DEBOUNCE_TIMER) clearTimeout(BILLING_POLL_DEBOUNCE_TIMER);
    BILLING_POLL_DEBOUNCE_TIMER = setTimeout(function() {
      BILLING_POLL_DEBOUNCE_TIMER = null;
      if (!document.hidden) pollBillingSnapshot(source);
    }, 300);
  }

  document.addEventListener('visibilitychange', function(){
    if (document.hidden) {
      stopBillingPolling();
      return;
    }
    debouncedPollOnVisible('billing-visible');
    startBillingPolling();
  });

  window.addEventListener('focus', function(){
    debouncedPollOnVisible('billing-focus');
  });

  if (payStatus === 'fail') {
    setTimeout(function(){
      if (window.MexionToast && window.MexionToast.show) {
        window.MexionToast.show(lang === 'zh' ? '支付未完成，请重试' : 'Payment was not completed', { tone: 'error' });
      }
    }, 120);
  }
  if (payStatus) clearBillingPayStatus();
});

