function reloadBillingTransactions() {
  if (typeof MexionHttp === 'undefined') return Promise.resolve();
  var lang = getBillingLang();
  return Promise.all([
    MexionHttp.get('/user/billing').catch(function(){ return { items: [], recentUsage: [] }; }),
    loadBillingUsageRecords().catch(function(){ return []; })
  ]).then(function(results){
    var data = results[0] || {};
    var usageRecords = Array.isArray(results[1]) ? results[1] : [];
    var items = data.items || [];
    var now = new Date();
    var thisMonthKey = now.getFullYear() + '-' + String(now.getMonth()+1).padStart(2,'0');
    var lastMonthDate = new Date(now.getFullYear(), now.getMonth() - 1, 1);
    var lastMonthKey = lastMonthDate.getFullYear() + '-' + String(lastMonthDate.getMonth()+1).padStart(2,'0');
    var monthlyTopup = 0;
    var monthlySpend = 0;
    var lastMonthSpend = 0;
    var topupCount = 0;
    var cumTopup = 0;
    var redeemItems = [];

    var topupItems = items.map(function(order){
      var type = orderType(order);
      var amt = parseBillingNumber(order.money != null ? order.money : (order.amount || 0)); // 二开:money=USD口径(amount是quota,rate≠1会金额错)
      var createdAt = order.created_at || order.create_time || order.date || ''; // 二开:后端 TopUp 时间字段是 create_time(epoch),原缺它→本月充值恒$0/0笔
      var monthKey = fmtMonthKey(createdAt);
      var settled = type === 'topup' ? isSettledTopup(order) : true;
      if (monthKey === thisMonthKey) {
        if ((type === 'topup' || type === 'redeem') && settled) {
          monthlyTopup += Math.abs(amt);
          topupCount++;
        } else if (type === 'usage') {
          monthlySpend += Math.abs(amt);
        }
      }
      if (monthKey === lastMonthKey && type === 'usage') {
        lastMonthSpend += Math.abs(amt);
      }
      if ((type === 'topup' || type === 'redeem') && settled) cumTopup += Math.abs(amt);
      if (type === 'redeem' && settled) {
        redeemItems.push({
          code: order.code || order.redeem_code || order.description || '—',
          amount: Math.abs(amt),
          date: fmtDate(createdAt)
        });
      }
      return {
        _type: type,
        _amount: amt,
        _dateShort: fmtDate(createdAt),
        _dateCsv: fmtCsvDate(createdAt),
        _time: fmtTime(createdAt),
        _monthKey: monthKey,
        _settled: settled,
        _desc: order.description || order.desc || order.title || '—',
        _sub: order.subtitle || order.method || order.payment_method || '',
        _balance: order.balance != null ? parseBillingNumber(order.balance) : null,
        _created: createdAt
      };
    });

    var usageItems = usageRecords.map(function(record) {
      var createdAt = usageCreatedAt(record);
      var monthKey = fmtMonthKey(createdAt);
      var amt = usageCost(record);
      if (monthKey === thisMonthKey) monthlySpend += Math.abs(amt);
      if (monthKey === lastMonthKey) lastMonthSpend += Math.abs(amt);

      var model = record.model || record.model_name || tt('billing.txn.desc.daily');
      var group = record.group || record.group_name || (record.groupId ? ('#' + record.groupId) : '');
      var tokens = Number(record.total_tokens || ((record.prompt_tokens || record.input_tokens || record.inputTokens || 0) + (record.completion_tokens || record.output_tokens || record.outputTokens || 0)) || 0);
      var subParts = [];
      if (group) subParts.push(group);
      if (tokens > 0) subParts.push(tokens.toLocaleString() + ' tok');

      return {
        _type: 'usage',
        _amount: Math.abs(amt),
        _dateShort: fmtDate(createdAt),
        _dateCsv: fmtCsvDate(createdAt),
        _time: fmtTime(createdAt),
        _monthKey: monthKey,
        _settled: true,
        _desc: model,
        _sub: subParts.join(' · '),
        _balance: null,
        _created: createdAt
      };
    });

    // 钱包=资金流水(充值/兑换/订阅)，不展示逐条模型消耗。
    // usageItems 仅用于聚合 KPI(本月/日均消耗) 与余额回溯计算，不进入展示列表。
    var byCreatedDesc = function(a,b){
      var da = parseBillingDate(a._created);
      var db = parseBillingDate(b._created);
      return (db ? db.getTime() : 0) - (da ? da.getTime() : 0);
    };
    var balanceTimeline = topupItems.concat(usageItems).sort(byCreatedDesc);
    TXN_ITEMS = topupItems.slice().sort(byCreatedDesc);

    var authUser = (typeof MexionAuth !== 'undefined' && MexionAuth.getUser) ? MexionAuth.getUser() : null;
    var currentBalance = authUser && typeof window.MexionQuota !== 'undefined'
      ? window.MexionQuota.getUserBalance(authUser)
      : BALANCE;
    var usedQuotaTotal = authUser && authUser.used_quota != null
      ? ((Number(authUser.used_quota) || 0) / 500000)
      : 0;
    if (cumTopup <= 0 && (currentBalance > 0 || usedQuotaTotal > 0)) {
      cumTopup = currentBalance + usedQuotaTotal;
    }
    // 余额回溯走完整时间线(含 usage)以保证展示的资金行余额正确，但只有资金行会被渲染。
    var runningBalance = currentBalance;
    balanceTimeline.forEach(function(item) {
      if (typeof item._balance !== 'number') {
        item._balance = runningBalance;
      }
      if (item._type === 'topup' || item._type === 'redeem') {
        runningBalance -= Math.abs(item._amount || 0);
      } else if (item._type === 'usage') {
        runningBalance += Math.abs(item._amount || 0);
      }
    });

    var kpiTopup = document.getElementById('kpiTopup');
    var kpiTopupDelta = document.getElementById('kpiTopupDelta');
    if (kpiTopup) kpiTopup.textContent = '$' + monthlyTopup.toFixed(0);
    if (kpiTopupDelta) kpiTopupDelta.textContent = topupCount + (lang === 'zh' ? ' 笔' : ' transaction' + (topupCount !== 1 ? 's' : ''));

    var kpiSpend = document.getElementById('kpiSpend');
    if (kpiSpend) {
      if (monthlySpend > 0) {
        var intPart = Math.floor(monthlySpend);
        var fracPart = String(Math.round((monthlySpend % 1) * 100)).padStart(2,'0');
        kpiSpend.innerHTML = '$' + intPart + '<span class="kpi__unit">.' + fracPart + '</span>';
      } else {
        kpiSpend.textContent = '$0';
      }
    }

    var kpiSpendDelta = document.getElementById('kpiSpendDelta');
    if (kpiSpendDelta) {
      if (lastMonthSpend > 0 && monthlySpend > 0) {
        var pct = ((monthlySpend - lastMonthSpend) / lastMonthSpend * 100).toFixed(0);
        kpiSpendDelta.textContent = (pct >= 0 ? '+' : '') + pct + '% ' + (lang === 'zh' ? '较上月' : 'vs last month');
      } else {
        kpiSpendDelta.textContent = lang === 'zh' ? '本月数据' : 'This month';
      }
    }

    var kpiDaily = document.getElementById('kpiDaily');
    var kpiDailyDelta = document.getElementById('kpiDailyDelta');
    var dayOfMonth = now.getDate();
    var dailyAvg = dayOfMonth > 0 ? monthlySpend / dayOfMonth : 0;
    if (kpiDaily) {
      if (dailyAvg > 0) {
        var dInt = Math.floor(dailyAvg);
        var dFrac = String(Math.round((dailyAvg % 1) * 100)).padStart(2,'0');
        kpiDaily.innerHTML = '$' + dInt + '<span class="kpi__unit">.' + dFrac + '</span>';
      } else {
        kpiDaily.textContent = '$0';
      }
    }
    if (kpiDailyDelta) kpiDailyDelta.textContent = lang === 'zh' ? '过去 ' + dayOfMonth + ' 天' : 'Past ' + dayOfMonth + ' days';

    var kpiCum = document.getElementById('kpiCum');
    if (kpiCum) kpiCum.textContent = '$' + cumTopup.toFixed(0);

    var kpiCumDelta = document.getElementById('kpiCumDelta');
    if (kpiCumDelta && TXN_ITEMS.length > 0) {
      var earliest = TXN_ITEMS[TXN_ITEMS.length - 1];
      var earliestDate = new Date(earliest._created || '');
      if (!isNaN(earliestDate.getTime())) {
        kpiCumDelta.textContent = (lang === 'zh' ? '自 ' : 'Since ') + earliestDate.getFullYear() + '-' + String(earliestDate.getMonth()+1).padStart(2,'0');
      }
    }

    var redeemMeta = document.getElementById('redeemMeta');
    if (redeemMeta) {
      redeemMeta.textContent = redeemItems.length > 0
        ? (lang === 'zh' ? '已兑换 ' + redeemItems.length + ' 次' : 'Used ' + redeemItems.length + ' time' + (redeemItems.length !== 1 ? 's' : ''))
        : (lang === 'zh' ? '尚未使用' : 'Not used yet');
    }

    var redeemHistBody = document.getElementById('redeemHistBody');
    if (redeemHistBody) {
      if (!redeemItems.length) {
        redeemHistBody.innerHTML = '<div style="color:var(--mute-3);font-size:11px;padding:6px 4px">' +
          (lang === 'zh' ? '暂无兑换记录' : 'No redemptions yet') + '</div>';
      } else {
        redeemHistBody.innerHTML = redeemItems.slice(0, 5).map(function(item) {
          var code = item.code || '—';
          var shortCode = code.length > 12 ? code.slice(0, 8) + '…' + code.slice(-4) : code;
          return '<div class="redeem-hist-row">' +
            '<span class="redeem-hist-code" title="' + code + '">' + shortCode + '</span>' +
            '<span class="redeem-hist-amt">+$' + item.amount.toFixed(2) + '</span>' +
            '<span class="redeem-hist-date">' + item.date + '</span>' +
          '</div>';
        }).join('');
      }
    }

    renderTxn(CURRENT_FILTER);
  }).catch(function(){
    renderTxn(CURRENT_FILTER);
  });
}

