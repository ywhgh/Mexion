  function fmtDate(s) {
    if (!s) return '-';
    var raw = String(s).trim();
    var d = /^\d{10}$/.test(raw)
      ? new Date(Number(raw) * 1000)
      : /^\d{13}$/.test(raw)
      ? new Date(Number(raw))
      : new Date(raw);
    if (isNaN(d.getTime())) return '-';
    return d.getFullYear() + '-' + String(d.getMonth()+1).padStart(2,'0') + '-' + String(d.getDate()).padStart(2,'0');
  }

